package com.zilker.bean;

public class RegisterData {
	
	private String user_id;
	
	private String user_name;
	
	private String pass_word;
	
	private String confirm_pass_word;
	
	private String e_mail;
	
	private String phone_no;
	
	private int role_id;
	
	private String role_name;
	
	private int acc_status;

	public String getUser_id() {
		return user_id;
	}

	public void setUser_id(String user_id) {
		this.user_id = user_id;
	}

	public String getUser_name() {
		return user_name;
	}

	public void setUserName(String user_name) {
		this.user_name = user_name;
	}

	public String getPassword() {
		return pass_word;
	}

	public void setPassword(String pass_word) {
		this.pass_word = pass_word;
	}

	public String getConfirm_pass_word() {
		return confirm_pass_word;
	}

	public void setConfirmPassword(String confirm_pass_word) {
		this.confirm_pass_word = confirm_pass_word;
	}

	public String getE_mail() {
		return e_mail;
	}

	public void setEmail(String e_mail) {
		this.e_mail = e_mail;
	}

	public int getRole_id() {
		return role_id;
	}

	public void setRole_id(int role_id) {
		this.role_id = role_id;
	}

	public String getRole_name() {
		return role_name;
	}

	public void setRole_name(String role_name) {
		this.role_name = role_name;
	}

	public int getAcc_status() {
		return acc_status;
	}

	public void setAcc_status(int acc_status) {
		this.acc_status = acc_status;
	}

	public String getPhone_no() {
		return phone_no;
	}

	public void setPhoneNo(String phone_no) {
		this.phone_no = phone_no;
	}
	
	
	
	

}
