package com.zilker.collections_programs;

public class InputData {
	private int id;
	private int appleCount;
	private int mangoCount;
	private int bananaCount;
	private int smallFishCount;
	private int bigFishCount;
	private int largeFishCount;
	public int getId() {
		return this.id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public int getAppleCount() {
		return appleCount;
	}
	public void setAppleCount(int appleCount) {
		this.appleCount = appleCount;
	}
	public int getMangoCount() {
		return mangoCount;
	}
	public void setMangoCount(int mangoCount) {
		this.mangoCount = mangoCount;
	}
	public int getBananaCount() {
		return bananaCount;
	}
	public void setBananaCount(int bananaCount) {
		this.bananaCount = bananaCount;
	}
	public int getSmallFishCount() {
		return smallFishCount;
	}
	public void setSmallFishCount(int smallFishCount) {
		this.smallFishCount = smallFishCount;
	}
	public int getBigFishCount() {
		return bigFishCount;
	}
	public void setBigFishCount(int bigFishCount) {
		this.bigFishCount = bigFishCount;
	}
	public int getLargeFishCount() {
		return largeFishCount;
	}
	public void setLargeFishCount(int largeFishCount) {
		this.largeFishCount = largeFishCount;
	}
}
