package io.ztech.fitnessapplication.beans;

public class UserPhysicalDetails {
	
	private String userName,gender;
	private int age,activty,bmr,plan;
	private float height,weight,bmi;
	public String getUserName() {
		return userName;
	}
	public void setUserName(String userName) {
		this.userName = userName;
	}
	public String getGender() {
		return gender;
	}
	public void setGender(String gender) {
		this.gender = gender;
	}
	public int getAge() {
		return age;
	}
	public void setAge(int age) {
		this.age = age;
	}
	public int getActivty() {
		return activty;
	}
	public void setActivty(int activty) {
		this.activty = activty;
	}
	public int getBmr() {
		return bmr;
	}
	public void setBmr(int bmr) {
		this.bmr = bmr;
	}
	public int getPlan() {
		return plan;
	}
	public void setPlan(int plan) {
		this.plan = plan;
	}
	public float getHeight() {
		return height;
	}
	public void setHeight(float height) {
		this.height = height;
	}
	public float getWeight() {
		return weight;
	}
	public void setWeight(float weight) {
		this.weight = weight;
	}
	public float getBmi() {
		return bmi;
	}
	public void setBmi(float bmi) {
		this.bmi = bmi;
	}
	

}
